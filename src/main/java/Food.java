public class Food {

    private char food;
    private int x;
    private int y;

    public Food(char food, int x, int y) {
        this.food = food;
        this.x = x;
        this.y = y;
    }

    public char getFood() {
        return food;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}
